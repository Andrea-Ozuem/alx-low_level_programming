#include "3-calc.h"

/**
  *op_add - adds 2 ints
  *@a: num
  *@b: num
  *Return: sum
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  *op_sub - subtracts 2 ints
  *@a: num
  *@b: num
  *Return: anser
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  *op_mul - multiplies
  *@a: num1
  *@b: num2
  *Return: product
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  *op_div- divides 2 ints
  *@a: num
  *@b: num
  *Return: answer
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  *op_mod - evaluates modulo of 2 num
  *@a: int
  *@b: int
  *Return: answer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
